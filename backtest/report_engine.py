"""回测报告引擎 — 生成 HTML 可视化报告。

功能：
1. 净值曲线（策略 vs 基准 vs 买入持有）
2. 回撤曲线
3. 交易记录明细表（含买卖点标注）
4. 关键指标仪表盘（年化收益/最大回撤/夏普比率/胜率/盈亏比）
5. 决策时间线（L1 触发事件）
6. 每日仓位变化图

使用纯 HTML + CSS + Chart.js (CDN) 渲染，无需后端服务。
"""

import json
import logging
from typing import Dict, List, Optional
from datetime import datetime

logger = logging.getLogger(__name__)

